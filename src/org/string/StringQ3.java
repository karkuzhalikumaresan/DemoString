package org.string;

public class StringQ3 {
	public static void main(String []args) {
		//print the character h 
		String s1="GreensTechnology";
		int i=s1.indexOf('h');
		char c=s1.charAt(i);
		System.out.println(c);
		
		//print the character o
		String s2="SeleniumAutomationtool";
		int i1=s2.indexOf('o');
		char c1=s2.charAt(i1);
		System.out.println(c1);
		
		//print the character u
		String s3="velmurugan";
		int index1=s3.indexOf('u');
		char c2=s3.charAt(index1);
		System.out.println(c2);
		
		//print the character p
		String s4="j a v a p r o g r a m";
		int index2=s4.indexOf('p');
		char c3=s4.charAt(index2);
		System.out.println(c3);
		
		//print the character 7
		String s5="9095484678";
		int index3=s5.indexOf("7");
		char c4=s5.charAt(index3);
		System.out.println(c4);
		
	}

}
